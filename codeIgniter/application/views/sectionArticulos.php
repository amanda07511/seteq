                  <h2 class="text-uppercase">Facebook: trucos para configurar tu privacidad</h2>

                  <h4>Elegir quién puede ver nuestras fotos</h4>
                  <br>
                  <p align="justify">El primer paso para limitar nuestra privacidad es elegir las personas que pueden tener acceso a nuestros contenidos publicados, especialmente en lo referente a fotografías o vídeos. Y es que Facebook otorga acceso ilimitado a todo el que quiera visionar nuestros posts hasta que nosotros lo decidamos; así, y mediante el acceso al menú de la esquina superior derecha, podemos decidir qué grupos de personas tienen acceso a nuestras publicaciones, ya sea público, amigos, amigos con excepciones, amigos concretos o sólo nosotros.

                  <h4>Comprobar lo que el resto ve de nuestro perfil</h4>
                  <br>
                  <p align="justify">¿Qué ven el resto de usuarios de nuestro perfil? Una buena manera de saber qué compartimos o no a través de nuestra página principal es realizar un visionado rápido de la misma. Para comprobarlo podemos acceder al icono “Ver como” debajo de la foto de perfil; así, podremos ver nuestra página como si fuéramos otro usuario y comprobar si queremos modificar algo de nuestra privacidad.

                  <h4>Comprobar si otros usuarios ven nuestra lista de amigos</h4>
                  <br>
                  <p align="justify">Si no queremos compartir nuestra lista de amigos con el resto de usuarios de Facebook siempre podemos acceder a Configuración de la cuenta y modificar los elementos que queremos mostrar y los que no, entre ellos, si queremos que los demás vean nuestra lista de amigos.

                  <h4>Ocultar un mensaje que no queremos ver publicado</h4>
                  <br>
                  <p align="justify">Si publicamos un post que no queremos compartir con el resto de usuarios de Facebook siempre podemos ocultarlo o eliminarlo de nuestra biografía. Eso sí, podrá seguir siendo accesible para otros si tiene alguna interacción, pero desaparecerá de nuestro muro para dejar de estar visible para el resto. Para ello tan sólo debemos ocultar el post a través de las opciones de publicación del propio mensaje.
